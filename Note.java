public class Note {
    private double wert;
    private double gewichtung;

    public Note(double wert, double gewichtung) {
        this.wert = wert;
        this.gewichtung = gewichtung;
    }

    public double getWert() {
        return wert;
    }

    public double getGewichtung() {
        return gewichtung;
    }
}
